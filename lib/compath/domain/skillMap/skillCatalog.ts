/**
 * スキルカタログ
 *
 * 製造業における技能伝承に必要なスキル体系を定義する。
 * ものづくり現場の問題解決能力を中心に、QCDE（品質・コスト・納期・環境）の
 * 観点から習熟度を測定する設計。
 *
 * 9カテゴリ x 8項目 = 72項目（ヒートマップが長方形になるよう均等配置）
 *
 * 各スキルに 基礎→実践→応用→熟練 の4段階レベル記述を持つ。
 * 「次にどんな問題を解決できればレベルが上がるか」を明示する設計。
 */

