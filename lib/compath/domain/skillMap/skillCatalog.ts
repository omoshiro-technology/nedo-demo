// ============================================================
// ヘルパー: レベル記述の簡易生成
// ============================================================

function lv(
  d1: string, n1: string,
  d2: string, n2: string,
  d3: string, n3: string,
  d4: string, n4: string,
): Record<SkillLevel, LevelDescription> {
  return {
    1: { description: d1, nextStep: n1 },
    2: { description: d2, nextStep: n2 },
    3: { description: d3, nextStep: n3 },
    4: { description: d4, nextStep: n4 },
  };
}

